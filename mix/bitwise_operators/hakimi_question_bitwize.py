def find_the_n_bit(num, n):
    """
    give a binary 1011 and find the nth bit

    00000000000000000000000000001111 
    00000000000000000000000000000001
    """
    bit = (num >> n-1) & 1
    return bit


print(find_the_n_bit(120, 4))


def flip_the_n_bit(num, n):
    """
    00000000000000000000000001111
    00000000000000000000000000001
    00000000000000000000000001110



    00000000000000000000000001111000
    00000000000000000000000000001000
    00000000000000000000000001111000
    00000000000000000000000001110000

    """
    bit = (num >> n-1) ^ 1
    bit = bit << n-1
    return bit


print(flip_the_n_bit(120, 4))

